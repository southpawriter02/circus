# Control Spotify from the command line
alias spotify-play="osascript -e 'tell application \"Spotify\" to play'"
alias spotify-pause="osascript -e 'tell application \"Spotify\" to pause'"
alias spotify-next="osascript -e 'tell application \"Spotify\" to next track'"
alias spotify-prev="osascript -e 'tell application \"Spotify\" to previous track'"
