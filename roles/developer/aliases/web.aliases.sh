# nvm (Node Version Manager)
alias nvmls="nvm ls"
alias nvmlsr="nvm ls-remote"
alias nvmu="nvm use"
alias nvmi="nvm install"

# npm (Node Package Manager)
alias npmi="npm install"
alias npmu="npm uninstall"
alias npmr="npm run"
